/* 
    Shorthand Operators: +=, -=, *=, /=, %=
    += add and assign
    -= subtract and assign
    *= multiply and assign
    /= divide and assign
    %= modulus and assign
*/

// Addition
let a = 10;
a += 5; // a = a + 5
console.log("a: " + a);

// subtraction
let b = 10;
b -= 5; 
console.log("b: " + b);

// Multiplication
let multiplyValue = 12;
multiplyValue *= 2;
console.log("multiplyValue: " + multiplyValue);

// Division
let value1 = 70;
value1 /= 10;
console.log("division: " + value1);

// Modulus
let value2 = 5;
value2 %= 2;
console.log("Modulus: " + value2);

// Exponent
let value3 = 25;
value3 **= 2;
console.log("Exponent: " + value3);

// increment: ++
let num = 5;
num++;
console.log("Increment: " + num);

// decrement: --
let num1 = 5;
num1--;
console.log("Decrement: " + num1);